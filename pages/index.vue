        if (new Date().getTime() - lastMouseCap < 10) return
        lastMouseCap = new Date().getTime()

        if (mousePos.length === 0) {
          stopwatch = new Date().getTime()
        }

        // Convert the mouse position to a percentage of the screen
        let x = e.clientX / window.innerWidth
        let y = e.clientY / window.innerHeight

        if (mousePos.length === 0) {
          mousePos.push({
            x: x,
            y: y,
            time: 0,
          })
        } else {
          let lastPos = mousePos[mousePos.length - 1]
          if (lastPos.x !== x || lastPos.y !== y) {
            mousePos.push({
              x: x,
              y: y,
              time: (new Date().getTime() - stopwatch) / 1000,
            })
          }

