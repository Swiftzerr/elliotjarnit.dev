      let last = {
        x: -1,
        y: -1,
        time: -1
      }
      let intervalsPassed = 0
      let interval = setInterval(() => {
        let currentTime = new Date().getTime() / 1000
        let timeElapsed = currentTime - refTime

        if (timeElapsed >= list[0].time) {
          let x = list[0].x * 100
          let y = list[0].y * 100

          let time = intervalsPassed / 100
          cursor.style.transition = `transform ${time}s linear`
          cursor.style.transform = `translate(${x}vw, ${y}vh)`

          last = list[0]
          list.shift()
          intervalsPassed = 0
        } else {
          intervalsPassed++
        }
        if (list.length === 0) {
